  const baseurl = "https://v6.exchangerate-api.com/v6/9a9546d65a01e63636982140/latest";
  const dropdowm = document.querySelectorAll(".dropdowm select");
  const formcurr = document.querySelector(".form select");
  const tocurr = document.querySelector(".to select");
  const btn = document.querySelector("form button");
  const msg = document.querySelector(".msg");
  
  for (let select of dropdowm) {
    for (let currcode in countryList) {
      let newOption = document.createElement("option");
      newOption.innerText = currcode;
      newOption.value = currcode;
  
      if (select.name === "form" && currcode === "USD") {
        newOption.selected = true;
      } else if (select.name === "to" && currcode === "INR") {
        newOption.selected = true;
      }
  
      select.appendChild(newOption);
    }
  
    select.addEventListener("change", (evt) => {
      updateFlag(evt.target);
    });
  }
  
  const updateFlag = (element) => {
    let currcode = element.value;
    let countrycode = countryList[currcode];
    let newsrc = `https://flagsapi.com/${countrycode}/flat/64.png`;
    let img = element.parentElement.querySelector("img");
    if (img) {
      img.src = newsrc;
    }
  };
  
  const updateExchange = async () => {
    let amount = document.querySelector(".amount input");
    let amountval = amount.value;
  
    if (amountval === "" || amountval < 1) {
      amountval = 1;
      amount.value = "1";
    }
  
    const URL = `${baseurl}/${formcurr.value}`;
    try {
      const response = await fetch(URL);
      const data = await response.json();
  
      const rate = data.conversion_rates[tocurr.value];
      const converted = (amountval * rate).toFixed(2);
  
      msg.innerText = `${amountval} ${formcurr.value} = ${converted} ${tocurr.value}`;
    } catch (err) {
      console.error("Fetch error:", err);
      msg.innerText = "Error fetching exchange rate.";
    }
  };
  
  btn.addEventListener("click", (evt) => {
    evt.preventDefault();
    updateExchange();
  });
  window.addEventListener("load", () => {
    updateFlag(formcurr);
    updateFlag(tocurr);
    updateExchange();
  });